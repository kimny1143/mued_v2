// 期待される出力形式
export interface HLAOutput {
  // カテゴリ分類された項目
  items: {
    category: 'idea' | 'todo' | 'tips' | 'frustration';
    content: string;
    // 元の文脈を保持（深度維持）
    context?: string;
  }[];

  // セッション全体の要約（深度を保つ）
  summary: {
    mainFocus: string;      // このセッションで主に何をしていたか
    keyInsights: string[];  // 重要な気づき（圧縮しすぎない）
    mood: 'productive' | 'creative' | 'frustrated' | 'learning' | 'mixed';
    nextActions?: string[]; // 明確なTODOがあれば
  };

  // メタ情報
  meta: {
    totalItems: number;
    categoryCounts: Record<string, number>;
    processingNote?: string; // 処理時の特記事項
  };
}
