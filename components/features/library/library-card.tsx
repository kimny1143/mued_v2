'use client';

import { useState } from 'react';
import Link from 'next/link';
import type { UnifiedContent } from '@/types/unified-content';
import { ExternalLinkModal } from './external-link-modal';

interface LibraryCardProps {
  content: UnifiedContent;
}

export function LibraryCard({ content }: LibraryCardProps) {
  const [showExternalModal, setShowExternalModal] = useState(false);

  const sourceColors = {
    note: 'bg-green-100 text-green-800',
    youtube: 'bg-red-100 text-red-800',
    ai_generated: 'bg-purple-100 text-purple-800',
    internal: 'bg-blue-100 text-blue-800',
    partner: 'bg-orange-100 text-orange-800',
  };

  const difficultyColors = {
    beginner: 'bg-emerald-100 text-emerald-800',
    intermediate: 'bg-yellow-100 text-yellow-800',
    advanced: 'bg-red-100 text-red-800',
  };

  const isExternal = content.source === 'note' || content.source === 'youtube' || content.source === 'partner';

  const handleLinkClick = (e: React.MouseEvent) => {
    if (isExternal) {
      const skipWarning = localStorage.getItem('mued_skip_external_warning') === 'true';
      if (!skipWarning) {
        e.preventDefault();
        setShowExternalModal(true);
      }
    }
  };

  const handleConfirmExternal = () => {
    setShowExternalModal(false);
    if (content.url) {
      window.open(content.url, '_blank', 'noopener,noreferrer');
    }
  };

  return (
    <div className="bg-white rounded-lg border border-gray-200 overflow-hidden hover:shadow-md transition-shadow">
      {/* Thumbnail */}
      {content.thumbnail && (
        <div className="aspect-video bg-gray-100 overflow-hidden">
          <img
            src={content.thumbnail}
            alt={content.title}
            className="w-full h-full object-cover"
          />
        </div>
      )}

      {/* Content */}
      <div className="p-4">
        {/* Badges */}
        <div className="flex flex-wrap gap-2 mb-3">
          <span className={`px-2 py-1 rounded-full text-xs font-medium ${sourceColors[content.source]}`}>
            {content.source}
          </span>
          {content.difficulty && (
            <span className={`px-2 py-1 rounded-full text-xs font-medium ${difficultyColors[content.difficulty]}`}>
              {content.difficulty}
            </span>
          )}
          <span className="px-2 py-1 rounded-full text-xs font-medium bg-gray-100 text-gray-800">
            {content.type}
          </span>
        </div>

        {/* Title */}
        <h3 className="text-lg font-semibold text-[var(--color-text-primary)] mb-2 line-clamp-2">
          {content.title}
        </h3>

        {/* Description */}
        <p className="text-sm text-[var(--color-text-secondary)] mb-3 line-clamp-3">
          {content.description}
        </p>

        {/* Metadata */}
        <div className="flex items-center justify-between text-xs text-gray-500 mb-3">
          <span>{content.author?.name || 'Unknown'}</span>
          <span>{new Date(content.publishedAt).toLocaleDateString()}</span>
        </div>

        {/* Tags */}
        {content.tags && content.tags.length > 0 && (
          <div className="flex flex-wrap gap-1 mb-3">
            {content.tags.slice(0, 3).map((tag) => (
              <span
                key={tag}
                className="px-2 py-0.5 rounded text-xs bg-gray-100 text-gray-600"
              >
                #{tag}
              </span>
            ))}
            {content.tags.length > 3 && (
              <span className="px-2 py-0.5 rounded text-xs text-gray-400">
                +{content.tags.length - 3}
              </span>
            )}
          </div>
        )}

        {/* AI Transparency Metadata */}
        {content.source === 'ai_generated' && content.aiMetadata && (
          <div className="mb-3 p-3 bg-purple-50 border border-purple-200 rounded-md space-y-2">
            <p className="text-xs text-purple-700 font-bold mb-2">ğŸ¤– AIé€æ˜æ€§æƒ…å ±</p>

            {/* Generated By */}
            <div>
              <p className="text-xs text-purple-600 font-medium">ç”Ÿæˆãƒ¢ãƒ‡ãƒ«:</p>
              <p className="text-xs text-purple-800">
                {content.aiMetadata.generatedBy.provider} {content.aiMetadata.generatedBy.model}
              </p>
            </div>

            {/* Quality Score */}
            <div>
              <p className="text-xs text-purple-600 font-medium">å“è³ªã‚¹ã‚³ã‚¢:</p>
              <div className="flex gap-2 mt-1">
                <span className="text-xs px-2 py-0.5 bg-purple-100 text-purple-800 rounded">
                  å†ç”Ÿæ€§: {content.aiMetadata.qualityScore.playability}/10
                </span>
                <span className="text-xs px-2 py-0.5 bg-purple-100 text-purple-800 rounded">
                  å­¦ç¿’ä¾¡å€¤: {content.aiMetadata.qualityScore.learningValue}/10
                </span>
              </div>
            </div>

            {/* Source Context */}
            {content.aiMetadata.sourceContext && (
              <div>
                <p className="text-xs text-purple-600 font-medium">ç”Ÿæˆå…ƒ:</p>
                <Link
                  href={content.aiMetadata.sourceContext.url}
                  className="text-xs text-purple-800 hover:text-purple-900 hover:underline"
                >
                  {content.aiMetadata.sourceContext.articleTitle}
                </Link>
              </div>
            )}

            {/* Human Review Status */}
            {content.aiMetadata.humanReview && (
              <div>
                <p className="text-xs text-purple-600 font-medium">äººé–“ãƒ¬ãƒ“ãƒ¥ãƒ¼:</p>
                <span className={`text-xs px-2 py-0.5 rounded ${
                  content.aiMetadata.humanReview.status === 'approved'
                    ? 'bg-green-100 text-green-800'
                    : content.aiMetadata.humanReview.status === 'needs_revision'
                    ? 'bg-yellow-100 text-yellow-800'
                    : 'bg-red-100 text-red-800'
                }`}>
                  {content.aiMetadata.humanReview.status === 'approved' ? 'æ‰¿èªæ¸ˆã¿' :
                   content.aiMetadata.humanReview.status === 'needs_revision' ? 'ä¿®æ­£å¿…è¦' : 'å´ä¸‹'}
                </span>
              </div>
            )}
          </div>
        )}

        {/* Actions */}
        <div className="flex gap-2">
          {content.url && (
            <Link
              href={content.url}
              target={isExternal ? '_blank' : undefined}
              rel={isExternal ? 'noopener noreferrer' : undefined}
              onClick={handleLinkClick}
              className="flex-1 px-4 py-2 bg-[var(--color-brand-green)] hover:bg-[var(--color-brand-green-hover)] text-white rounded-md text-sm font-medium text-center transition-colors"
            >
              {content.source === 'ai_generated' || content.source === 'internal' ? 'View Details' : 'View Content'}
            </Link>
          )}
        </div>
      </div>

      {/* External Link Modal */}
      <ExternalLinkModal
        url={content.url || ''}
        sourceName={content.source === 'note' ? 'note.com' : content.source === 'youtube' ? 'YouTube' : content.source}
        isOpen={showExternalModal}
        onClose={() => setShowExternalModal(false)}
        onConfirm={handleConfirmExternal}
      />
    </div>
  );
}
